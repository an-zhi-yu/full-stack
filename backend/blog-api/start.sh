#!/bin/bash

# =============================================================================
# 【问题3解答】start.sh 脚本的用处
# 
# 这个脚本用于一键启动 Spring Boot 应用
# 类似于前端项目中的 npm run dev 或 npm start
# 
# 脚本功能：
# 1. 检查端口是否被占用（防止启动失败）
# 2. 构建项目（编译代码，打包 JAR）
# 3. 启动应用（运行 JAR 文件）
# 
# 前端类比：
# 类似于 package.json 中的 scripts：
# "scripts": {
#   "start": "npm run build && node dist/server.js",
#   "build": "webpack --mode production"
# }
# 
# 使用方式：
# 1. 给脚本添加执行权限：chmod +x start.sh
# 2. 运行脚本：./start.sh
# =============================================================================

# 定义端口号变量
# 这里使用 8080 端口，和前端开发服务器默认端口一致
PORT=8080

# =============================================================================
# 步骤1：检查端口占用
# 
# 为什么需要检查？
# 如果 8080 端口已经被其他程序占用，Spring Boot 会启动失败
# 类似于前端如果 3000 端口被占用，webpack-dev-server 会报错
# 
# 这个检查会：
# 1. 查看 8080 端口是否被占用
# 2. 如果被占用，显示占用进程的信息
# 3. 询问是否终止占用进程
# =============================================================================

# lsof 命令：列出打开的文件（Linux/Unix 中，网络端口也是文件）
# 参数说明：
# -Pi :$PORT：指定端口号，-P 表示显示端口号而不是服务名，-i 表示网络文件
# -sTCP:LISTEN：只显示处于监听状态的 TCP 连接
# -t：只显示进程 ID（PID），不显示其他信息
# >/dev/null 2>&1：将标准输出和错误输出重定向到空设备（不显示在屏幕上）
# 
# 前端类比：
# 类似于检查 3000 端口是否被占用：lsof -i :3000
if lsof -Pi :$PORT -sTCP:LISTEN -t >/dev/null 2>&1; then
    # 如果端口被占用，执行以下代码
    echo "端口 $PORT 已被占用，正在查找占用进程..."
    
    # 获取占用端口的进程 ID（PID）
    # $(...) 是命令替换，将命令的输出赋值给变量
    PID=$(lsof -Pi :$PORT -sTCP:LISTEN -t)
    echo "占用端口的进程ID: $PID"
    
    # 询问用户是否终止占用进程
    # read -p：显示提示信息并等待用户输入
    # 类似于前端的 confirm() 对话框
    read -p "是否终止占用端口的进程？(y/n): " answer
    
    # 判断用户输入
    if [ "$answer" = "y" ]; then
        # kill -9：强制终止进程（-9 是 SIGKILL 信号）
        # 类似于任务管理器中结束进程
        kill -9 $PID
        echo "进程已终止"
    else
        # 用户选择不终止，退出脚本
        echo "请手动处理端口占用问题"
        exit 1  # 退出状态码 1 表示异常退出
    fi
fi

# =============================================================================
# 步骤2：构建项目
# 
# mvn clean package：Maven 构建命令
# - clean：清理 target 目录（类似于删除 dist 目录）
# - package：打包项目（编译代码并生成 JAR 文件）
# - -DskipTests：跳过测试（加快构建速度）
# 
# 前端类比：
# 类似于 npm run build
# 它会：
# 1. 编译 Java 源代码（类似于 webpack 编译 JS/TS）
# 2. 运行测试（类似于 jest 测试）
# 3. 打包成 JAR 文件（类似于生成 dist 目录）
# 
# target 目录：
# 类似于前端的 dist 或 build 目录
# 存放编译后的文件和最终的 JAR 包
# =============================================================================
echo "正在构建项目..."
mvn clean package -DskipTests

# 检查构建是否成功
# $? 表示上一个命令的退出状态码
# 0 表示成功，非 0 表示失败
if [ $? -ne 0 ]; then
    echo "构建失败，请检查代码错误"
    exit 1
fi

# =============================================================================
# 步骤3：启动应用
# 
# java -jar：运行 JAR 文件
# target/blog-api-1.0.0.jar：生成的 JAR 文件路径
# 
# 前端类比：
# 类似于 node dist/server.js
# 或者 npm run start
# 
# 启动后：
# 1. Spring Boot 会启动内嵌的 Tomcat 服务器
# 2. 监听 8080 端口
# 3. 你可以通过 http://localhost:8080 访问应用
# 
# 【问题3解答】http://localhost:8080 访问说明：
# 
# 是的，启动后你可以通过 http://localhost:8080 访问这个服务
# 类似于前端开发服务器的访问方式
# 
# 访问方式：
# 1. 浏览器访问示例（返回 JSON）：
#    - http://localhost:8080/api/hello
#    - http://localhost:8080/api/learn/ping
# 2. 接口访问（AJAX/Fetch）：需处理跨域或走 Vite 代理，见 docs/前端学后端-REST与YAML配置.md
# 3. curl：curl http://localhost:8080/api/health
# 
# 这就是 RESTful API 的访问方式：
# - 后端提供接口（Controller 中的方法）
# - 前端通过 HTTP 请求访问这些接口
# - 数据通常以 JSON 格式传输（这里是简单的字符串）
# =============================================================================
echo "正在启动应用..."
echo "应用启动后，可以通过以下地址访问："
echo "  - 示例 GET：http://localhost:8080/api/hello"
echo "  - 健康检查：http://localhost:8080/api/health"
echo "  - 学习用 ping：http://localhost:8080/api/learn/ping"
echo ""
echo "按 Ctrl+C 停止应用"
echo ""

# 启动 JAR 文件
java -jar target/blog-api-1.0.0.jar

# =============================================================================
# 总结：
# 
# 这个脚本做了三件事：
# 1. 检查端口占用（避免启动失败）
# 2. 构建项目（编译 + 打包）
# 3. 启动应用（运行 JAR）
# 
# 前端类比：
# 类似于一个组合命令：npm run clean && npm run build && npm run start
# 
# 访问方式：
# - 启动后，http://localhost:8080 就可以访问
# - 这就是后端提供的 API 接口
# - 前端可以通过 AJAX、Fetch、Axios 等方式调用这些接口
# 
# target 目录：
# - 类似于前端的 dist 目录
# - 存放编译后的文件
# - 不需要手动管理，Maven 会自动处理
# =============================================================================
