/**
 * ============================================
 * 接口（Interface）：Shape 形状
 * ============================================
 *
 * 【接口是什么？】
 * 接口是一份「约定」：只规定「有什么方法、方法长什么样」，不写具体实现。
 * 谁想遵守这份约定，就用 implements 实现这个接口，并把这些方法都实现出来。
 *
 * 【和类的区别】
 * - 类（class）：可以有字段、有方法体（具体逻辑）。
 * - 接口（interface）：一般只有方法「声明」（名字+参数+返回值），没有方法体。
 *   实现类必须写出每个方法的具体逻辑。
 *
 * 【为什么要用接口？】
 * 多态：用「接口类型」的变量可以指向任何实现了该接口的类，调用同一方法名，运行时会执行各自实现。
 * 例如：Shape s1 = new Circle(); Shape s2 = new Rectangle(); s1.describe(); s2.describe();
 *      编译时只看「Shape 有没有 describe」；运行时 s1 实际是 Circle，就执行 Circle 的 describe。
 *
 * 【命名】
 * 接口名常用形容词或名词，这里用 Shape（形状）表示「一种可描述的形状」。
 */
public interface Shape {

    /**
     * 描述这个形状（例如 "圆形" / "矩形"）。
     * 接口里只写方法签名：返回值 + 方法名 + 参数，不写方法体（没有 { } 里的代码）。
     * 所有「实现这个接口」的类都必须实现这个方法，否则编译报错。
     *
     * @return 形状的描述文字
     */
    String describe();

    /**
     * 再定义一个方法：返回形状的边数（圆可以理解为 0 或 1，矩形为 4）。
     * 这样有两个方法的接口，实现类必须把两个都实现。
     *
     * @return 边数
     */
    int getEdgeCount();
}
